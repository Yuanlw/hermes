/**
 * @author
 * @description
 * @since 1.0
 */
public class LocalContextException extends RuntimeException {

    public LocalContextException() {
        super();

    }

    public LocalContextException(String message, Throwable cause) {
        super(message, cause);

    }

    public LocalContextException(String message) {
        super(message);

    }

    public LocalContextException(Throwable cause) {
        super(cause);

    }
}
